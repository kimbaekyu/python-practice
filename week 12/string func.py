# 문자열 함수 사용하기
str="Work hard, play hard"
print("String : %s"%str)
print("upper(): %s"%str.upper())
print("")

print("String : %s"%str)
print("lower(): %s"%str.lower())
print("")

print("String : %s"%str)
print("swapcase(): %s"%str.swapcase())
print("")

print("String : %s"%str)
print("title(): %s"%str.title())
print("")

print("String : %s"%str)
print("count('a'): %s"%str.count('a'))
print("")

print("String : %s"%str)
print("find('p'): %s"%str.find('p',0))
print("")

print("String : %s"%str)
print("rfind('p'): %s"%str.rfind('p',0))
print("")

print("String : %s"%str)
print("index('p'): %s"%str.index('p',0))
print("")

print("String : %s"%str)
print("startswith('p'): %s"%str.startswith('p'))
print("")

print("String : %s"%str)
print("endswith('p'): %s"%str.endswith('p'))
print("")
